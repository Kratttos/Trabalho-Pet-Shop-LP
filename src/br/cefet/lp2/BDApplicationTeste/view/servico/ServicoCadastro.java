    private void bExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluirActionPerformed
        int linha = tabelaServicos.getSelectedRow();
        DefaultTableModel tModel = (DefaultTableModel) tabelaServicos.getModel();
        int cod = getCodServicoFromTable();
        
        int opt = JOptionPane.showConfirmDialog(this, "Confirma a exclusão do Servico de código " + cod + "?");
        if (opt == JOptionPane.OK_OPTION){
            try{
                ServicoDao dao = new ServicoDao();
                dao.excluir(cod);
                
                tModel.removeRow(linha);
                
                bAlterar.setEnabled(false);
                bExcluir.setEnabled(false);
            } catch(Exception e){
                JOptionPane.showMessageDialog(this, "Erro ao excluir Servico :- " +e.getMessage(), "Erro de Excluão", JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_bExcluirActionPerformed

    private void bAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAlterarActionPerformed
        int linha = tabelaServicos.getSelectedRow();
        DefaultTableModel tModel = (DefaultTableModel) tabelaServicos.getModel();
        int cod = getCodServicoFromTable();
        
        ServicoAlterar dialog = new ServicoAlterar(this, true, cod);
        dialog.setVisible(true);
        
        Servico p = dialog.getServico();
        if (p != null){
            tModel.setValueAt(p.getCod()+"", linha, 0);
            tModel.setValueAt(p.getDescricao(), linha, 1);
            tModel.setValueAt(p.getValor(), linha, 2);
            
        }
        
        
    }//GEN-LAST:event_bAlterarActionPerformed

    public int getCodServicoFromTable(){
        int linha = tabelaServicos.getSelectedRow();
        
        DefaultTableModel tModel = (DefaultTableModel) tabelaServicos.getModel();
        
        String codStr = tModel.getValueAt(linha, 0).toString();
        int cod = Integer.parseInt(codStr);
        
        return  cod;
    }
    
