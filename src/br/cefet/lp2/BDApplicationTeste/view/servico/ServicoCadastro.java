    private void preencherJTable(List<Servico> pList) {
        String[] colunas = new String[]{"Cod.", "Descrição", "Valor"};

        String[][] dados = new String[pList.size()][5];
        for (int i = 0; i < pList.size(); i++) {
            Servico p = pList.get(i);
            dados[i][0] = p.getCod() + "";
            dados[i][1] = p.getDescricao();
            dados[i][2] = p.getValor()+"";
        }

        tabelaServicos.setModel(new javax.swing.table.DefaultTableModel(
                dados, colunas
        ));
        
        bAlterar.setEnabled(false);
        bExcluir.setEnabled(false);
    }
