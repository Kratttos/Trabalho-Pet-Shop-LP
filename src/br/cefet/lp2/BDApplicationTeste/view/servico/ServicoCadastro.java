    private void tFiltroKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tFiltroKeyPressed
        if (evt.getKeyCode() == 10) {
            try {
                List<Servico> pList = new LinkedList<>();
                
                ServicoDao dao = new ServicoDao();
                pList = dao.consultarPorDescricao(tFiltro.getText());
                
                preencherJTable(pList);
            } catch (DaoException ex) {
                Logger.getLogger(ServicoCadastro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tFiltroKeyPressed

