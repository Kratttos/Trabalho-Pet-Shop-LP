    private void preencherJTable(List<Pet> pList) {
        String[] colunas = new String[]{"Cod.", "Nome", "Dono", "Raça", "Dt. Nasc."};

        String[][] dados = new String[pList.size()][5];
        for (int i = 0; i < pList.size(); i++) {
            Pet p = pList.get(i);
            dados[i][0] = p.getCod() + "";
            dados[i][1] = p.getNome();
            dados[i][2] = p.getDono();
            dados[i][3] = p.getRaca();
            dados[i][4] = DateParse.dateToString(p.getDtNasc(), "dd/MM/yyyy");
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                dados, colunas
        ));
        
        jButton2.setEnabled(false);
        jButton3.setEnabled(false);
    }
