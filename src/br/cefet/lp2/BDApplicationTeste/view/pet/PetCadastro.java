    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linha = jTable1.getSelectedRow();
        DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();
        int cod = getCodPetFromTable();
        
        int opt = JOptionPane.showConfirmDialog(this, "Confirma a exclusão do Pet de código " + cod + "?");
        if (opt == JOptionPane.OK_OPTION){
            try{
                PetDao dao = new PetDao();
                dao.excluir(cod);
                
                tModel.removeRow(linha);
                
                jButton2.setEnabled(false);
                jButton3.setEnabled(false);
            } catch(Exception e){
                JOptionPane.showMessageDialog(this, "Erro ao excluir pet :- " +e.getMessage(), "Erro de Excluão", JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linha = jTable1.getSelectedRow();
        DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();
        int cod = getCodPetFromTable();
        
        PetAlterar dialog = new PetAlterar(this, true, cod);
        dialog.setVisible(true);
        
        Pet p = dialog.getPet();
        if (p != null){
            tModel.setValueAt(p.getCod()+"", linha, 0);
            tModel.setValueAt(p.getNome(), linha, 1);
            tModel.setValueAt(p.getDono(), linha, 2);
            tModel.setValueAt(p.getRaca(), linha, 3);
            tModel.setValueAt( DateParse.dateToString( p.getDtNasc() ), linha, 4);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    public int getCodPetFromTable(){
        int linha = jTable1.getSelectedRow();
        
        DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();
        
        String codStr = tModel.getValueAt(linha, 0).toString();
        int cod = Integer.parseInt(codStr);
        
        return  cod;
    }
    
